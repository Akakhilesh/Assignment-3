import math
number = int(input("Enter a number: "))             # takes number as integer input
print(f"Square root : {math.sqrt(number)}")         # displays square root of the number
print(f"Logarithm : {math.log(number)}")            # displays logarithm of the number
print(f"Sine : {math.sin(number)}")                 # displays sine of the number in radians

